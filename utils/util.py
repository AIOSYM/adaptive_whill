def points_perspective_transform(x1, y1, x2, y2, homographyMatrix):
    oldPoints = np.array([[[x1, y1], [x2,y2]]], dtype='float32')
    newPoints = cv2.perspectiveTransform(oldPoints, homographyMatrix)
    (x1, y1), (x2, y2) = np.squeeze(newPoints, axis=0)
    return x1, y1, x2, y2


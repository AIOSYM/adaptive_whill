def get_controls(x, z, Kp_l, Ki_l, Kd_l, Kp_a, Ki_a, Kd_a, il, ia, dl, da):
    """
    PID controller - Proportional (P), Integral (I), Derivative (D)
    Kp: Proportional Gain
    Ki: Integral Gain
    Kd: Derivative Gain
    l: linear
    a: angular
    """

    i_error_l = il #integral error for linear
    i_error_a = ia #integral error for angular
    d_error_l = dl #derivative error for linear
    d_error_a = da #derivative error for angular

    #proportional error for linear
    p_error_l = z - 0.5 
    #proportional error for angular
    p_error_a = x - 640 

    if abs(p_error_l) < 0.2:
        p_error_l = 0
    if (0 < p_error_a < 150) or (-100 < p_error_a < 0):
        p_error_a = 0

    i_error_l += p_error_l
    i_error_a += p_error_a
    curr_d_error_l = p_error_l - d_error_l
    curr_d_error_a = p_error_a - d_error_a

    linear = Kp_l*p_error_l + Ki_l*i_error_l + Kd_l*curr_d_error_l
    angular = Kp_a*p_error_a + Ki_a*i_error_a + Kd_a*curr_d_error_a
    
    if linear > 0.6:
        linear = 0.6

    if angular > 0.3:
        angular = 0.3

    if linear < -0.6:
        linear = -0.6

    if angular < -0.3:
        angular = -0.3

    twist = Twist()

    twist.linear.x = linear
    twist.linear.y = 0
    twist.linear.z = 0

    twist.angular.x = 0
    twist.angular.y = 0
    twist.angular.z = angular
    
    print('linear: {} ,angular: {}  \n'.format(linear,angular))
    error = (i_error_l, i_error_a, d_error_l, d_error_a)

    return twist, error
    
def get_coordinates(box, x, y, x1, y1):
    """ 
    Get co-ordinates of flaged person
    """
    if len(box) == 0:
        print('No person detected!!!')
        return (0,0,0,0) , (0,0,0,0)

    iou_scores = []
    for i in range(len(box)):
        iou_scores.append(get_iou(box[i],[x,y,x1,y1]))

    index = np.argmax(iou_scores)
    #print(iou_scores, ' ',box, ' ', x, y, x1, y1)

    if np.sum(iou_scores) == 0:
        # print('#'*20, 'No Match found', '#'*20)
        box = np.array(box)
        distance = np.power(((x+x1)/2 - np.array(box[:,0] + box[:,2])/2),2) + np.power(((y+y1)/2 - (box[:,1]+box[:,3])/2), 2)
        index = np.argmin(distance)

    x, y, w, h = box[index][0], box[index][1], (box[index][2]-box[index][0]), (box[index][3]-box[index][1])
    initBB = (x+w//2-50,y+h//2-50,100,100)

    return initBB, (x,y,x+w,y+h)

def get_iou(boxA, boxB):
	""" Find iou of detection and tracking boxes
	"""
	# determine the (x, y)-coordinates of the intersection rectangle
	xA = max(boxA[0], boxB[0])
	yA = max(boxA[1], boxB[1])
	xB = min(boxA[2], boxB[2])
	yB = min(boxA[3], boxB[3])

	# compute the area of intersection rectangle
	interArea = max(0, xB - xA + 1) * max(0, yB - yA + 1)

	# compute the area of both the prediction and ground-truth
	# rectangles
	boxAArea = (boxA[2] - boxA[0] + 1) * (boxA[3] - boxA[1] + 1)
	boxBArea = (boxB[2] - boxB[0] + 1) * (boxB[3] - boxB[1] + 1)

	# compute the intersection over union by taking the intersection
	# area and dividing it by the sum of prediction + ground-truth
	# areas - the interesection area
	iou = interArea / float(boxAArea + boxBArea - interArea)

	# return the intersection over union value
	return iou

